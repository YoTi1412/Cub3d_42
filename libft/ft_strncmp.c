#include "../includes/libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	i = 0;
	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (i < n - 1 && (ptr1[i] == ptr2[i]) && ptr1[i] && ptr2[i])
	{
		i++;
	}
	return (ptr1[i] - ptr2[i]);
}
